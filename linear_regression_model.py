"""**Lasso Regression**"""

lasso=Lasso()

parameters={'alpha':[0.0001,0.001,0.01,0.1,1,10,50,100]}
grid=GridSearchCV(lasso, parameters, scoring='neg_mean_squared_error', cv=5)
grid.fit(X_train, y_train)

lasso_best=Lasso(alpha=grid.best_params_['alpha'],max_iter=10000)
lasso_best.fit(X_train, y_train)

preds=lasso_best.predict(X_test)

