st.title("🏠 California Housing Price Predictor")

st.write("Choose a model and enter house features to predict the median house value.")

model_choice = st.selectbox("Select Model", ["Linear Regression", "Ridge Regression", "Lasso Regression"])

# Input features
longitude = st.number_input("Longitude", value=-118.0)
latitude = st.number_input("Latitude", value=34.0)
housing_median_age = st.number_input("Housing Median Age", value=30)
total_rooms = st.number_input("Total Rooms", value=1000)
total_bedrooms = st.number_input("Total Bedrooms", value=300)
population = st.number_input("Population", value=1000)
households = st.number_input("Households", value=300)
median_income = st.number_input("Median Income", value=3.0)
ocean_proximity = st.selectbox(
    "Ocean Proximity",
    ["<1H OCEAN", "INLAND", "ISLAND", "NEAR BAY", "NEAR OCEAN"]
)

# Feature engineering
rooms_per_household = total_rooms / households
bedrooms_per_room = total_bedrooms / total_rooms
population_per_household = population / households

# One-hot encoding
ocean_features = ["INLAND", "ISLAND", "NEAR BAY", "NEAR OCEAN", "<1H OCEAN"]
ocean_input = [1 if ocean_proximity == feat else 0 for feat in ocean_features]

# Combine features
X_input = np.array([
    longitude, latitude, housing_median_age, total_rooms, total_bedrooms,
    population, households, median_income,
    rooms_per_household, bedrooms_per_room, population_per_household,
    *ocean_input
]).reshape(1,-1)

# Scale features
X_input_scaled = scaler.transform(X_input)

# Predict button
if st.button("Predict Price"):
    if model_choice == "Linear Regression":
        price = linear_model.predict(X_input_scaled)[0]
    elif model_choice == "Ridge Regression":
        price = ridge_model.predict(X_input_scaled)[0]
    else:
        price = lasso_model.predict(X_input_scaled)[0]
    st.success(f"💰 Predicted Median House Value: ${price:,.2f}")

