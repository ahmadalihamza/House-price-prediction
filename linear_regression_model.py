coeffs = pd.DataFrame({
    "Feature": X.columns,
    "Coefficient": ridge_best.coef_
})

# Sort by absolute coefficient
coeffs['abs_coef'] = coeffs['Coefficient'].abs()
coeffs = coeffs.sort_values(by='abs_coef', ascending=False).head(15)

plt.figure(figsize=(10,6))
sns.barplot(x='abs_coef', y='Feature', data=coeffs, palette='viridis')
plt.xlabel("Absolute Coefficient Value")
plt.title("Top 15 Feature Importance (Ridge Regression)")
